function reverseName(nameOne, nameTwo) {
    let friendOneLength = nameOne.length;
    let friendTwoLength = nameTwo.length;
    let largeName = '';

    if (friendOneLength > friendTwoLength) {
        largestName = nameOne;

    } else {
        largestName = nameTwo;
    } return largestName

}
